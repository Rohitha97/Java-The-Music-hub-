    private void textSKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textSKeyPressed
        if(evt.getKeyCode()==KeyEvent.VK_ENTER){
            PreparedStatement ps;
            ResultSet rs;
            try{

                ps = DBconnection.getConnection().prepareStatement("SELECT * FROM `soloregister` WHERE `sUName`=?");
                ps.setString(1,textS.getText());
                rs=ps.executeQuery();

                if(rs.next()){
                    sFName.setText(rs.getString(2));
                    sLName.setText(rs.getString(3));
                    sUName.setText(rs.getString(4));
                    sPWD.setText(rs.getString(5));
                    sPNo.setText(rs.getString(6));
                    sEmail.setText(rs.getString(7));
                    sLocation.setText(rs.getString(8));
                    sAge.setText(rs.getString(9));
                    
                    if(rs.getString(10).equals("Male")){
                        sMale.setSelected(true);
                        sFemale.setSelected(false);
                    }else if(rs.getString(10).equals("Female")){
                        sMale.setSelected(false);
                        sFemale.setSelected(true);
                    }
                    
                    if(rs.getString(11).equals("Pop")){
                        pop.setSelected(true);
                    }if(rs.getString(11).equals("Rock")){
                        rock.setSelected(true);
                    }if(rs.getString(11).equals("R&B")){
                        rb.setSelected(true);
                    }if(rs.getString(11).equals("Classical")){
                        classical.setSelected(true);
                    }if(rs.getString(11).equals("Country")){
                        country.setSelected(true);
                    }if(rs.getString(11).equals("Hip-hop")){
                        hiphop.setSelected(true);
                    }if(rs.getString(11).equals("Other Genres")){
                        other.setSelected(true);
                    }
                    
                    sBio.setText(rs.getString(12));
                    sCharge.setText(rs.getString(13));
                }
            } catch (SQLException ex) {
                Logger.getLogger(sUpdateProfile.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_textSKeyPressed

