    private void sSigninActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sSigninActionPerformed

        String sfname=sFName.getText();
        String slname=sLName.getText();
        String suname = sUName.getText();
        String spass = String.valueOf(sPWD.getPassword());
        String sphone = sPNo.getText();
        String semail = sEmail.getText();
        String saddress = sLocation.getText();
        String sage = sAge.getText();

        String sgender = "Male";
        if(sFemale.isSelected()){
            sgender = "Female";
        }

        String sgenre = " ";
        if(rock.isSelected()){
            sgenre= rock.getText()+" ";
        }
        if(classical.isSelected()){
            sgenre += classical.getText()+" ";
        }
        if(country.isSelected()){
            sgenre += country.getText()+" ";
        }
        if(rb.isSelected()){
            sgenre += rb.getText()+" ";
        }

        if(hiphop.isSelected()){
            sgenre += hiphop.getText()+" ";
        }
        if(pop.isSelected()){
            sgenre += pop.getText()+" ";
        }
        if(other.isSelected()){
            sgenre += other.getText()+" ";
        }

        String sbio = sBio.getText();
        String scharge = sCharge.getText();


            PreparedStatement ps;
            int p = JOptionPane.showConfirmDialog(null, "Are you sure to update this record ?","Update Recoed",JOptionPane.YES_NO_OPTION);
            
            if(p==0){
               
                try { 
                   String query = "UPDATE soloregister SET sFName=?,sLName=?,sUName=?,sPWD=?,sPNo=?,sEmail=?,sLocation=?,sAge=?,sGender=?,sGenre=?,sBio=?,sCharge=? WHERE sUName=?";
                    ps = DBconnection.getConnection().prepareStatement(query);

                    ps.setString(1, sfname);
                    ps.setString(2, slname);
                    ps.setString(3, suname);
                    ps.setString(4, spass);
                    ps.setString(5, sphone);
                    ps.setString(6, semail);
                    ps.setString(7, saddress);
                    ps.setString(8, sage);
                    ps.setString(9, sgender);
                    ps.setString(10, sgenre);
                    ps.setString(11, sbio);
                    ps.setString(12, scharge);
                    ps.setString(13, suname); 
                    ps.executeUpdate();

                    JOptionPane.showMessageDialog(null,"Data Updated Successfully");
                    dispose();
                } catch (SQLException ex) {
                       JOptionPane.showMessageDialog(null,ex);
                }
            }
    }//GEN-LAST:event_sSigninActionPerformed

